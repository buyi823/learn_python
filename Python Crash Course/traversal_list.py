magicians = ['alice', 'david', 'carolina']
for magician in magicians:
    print(f"{magician.title()}, that was a great trick!")
    print(f"I can't wait to see your next trick, {magician.title()}.\n")

print("Thank you, everyone. That was a great magic show!\n")

pizzas = ['pepperoni', 'Chicago-Style', 'New York Style']
for pizza in pizzas:
    print(f"I like {pizza.title()} pizza.")

print("I really love pizza!\n")

pets = ['dog', 'cat', 'rabbit', 'chicken', 'hamster', 'fish', 'pig']
for pet in pets:
    print(f"A {pet} would make a great pet.")

print("Any of these animals would make a great pet!\n")